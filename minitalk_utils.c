#include "minitalk.h"

int	ft_atoi(char *str)
{
	int	num;

	num = 0;
	while (*str <= 0 && *str >= 9)
	{
		if (str > 9 || str < 0)
			return (0);
		num += num * 10;
		str++;
	}
	return (num);
}